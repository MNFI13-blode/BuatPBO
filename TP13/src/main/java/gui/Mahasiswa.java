    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        // TODO add your handling code here:
        String nimUP = nimUpdate.getText();
        String namaUP = namaUpdate.getText();
        String prodiUP = radio_dsUpdate.isSelected() ? "DS" : radio_seUpdate.isSelected() ? "SE" : radio_itUpdate.isSelected() ? "IT" : radio_ifupdate.isSelected() ? "IF" : "";

        if(!nimUP.isEmpty() && !namaUP.isEmpty() && !prodiUP.isEmpty()){
            try{
                int id = selectedM.getId();
                mahasiswa mah = new mahasiswa(nimUP,namaUP,prodiUP,id);
                if(selectedM != null){
                    mahService.Update(mah);
                    JOptionPane.showMessageDialog(null, "Data berhasil di update!");
                    nimUpdate.setText("");
                    namaUpdate.setText("");
                    radio_dsUpdate.setSelected(false);
                    radio_seUpdate.setSelected(false);
                    radio_itUpdate.setSelected(false);
                    radio_ifupdate.setSelected(false);
                    reload();
                    jTabbedPane1.setSelectedIndex(0);
                }
            }catch (SQLException ex){
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Semua field harus diisi", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4MouseClicked

