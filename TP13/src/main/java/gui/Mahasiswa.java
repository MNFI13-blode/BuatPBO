    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // TODO add your handling code here:
        int selectIndex = jList1.getSelectedIndex();

        if(selectIndex != -1){
            mahasiswa selectedNama = getSelectedMaha();
            try{
                mahService.delete(selectedNama.getNama());
                JOptionPane.showMessageDialog(null, "Mahasiswa dengan Nama " + selectedNama.getNama() + " berhasil dihapus.");
                reload();
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Pilih mahasiswa yang ingin dihapus!");
        }
    }//GEN-LAST:event_jButton5MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        selectedM = getSelectedMaha();
        if(selectedM != null){
            //           jTabbedPane1.setSelectedIndex(2);
            nimUpdate.setText(selectedM.getNim());
            namaUpdate.setText(selectedM.getNama());
            switch(selectedM.getProdi()){
                case "IF" -> radio_ifupdate.setSelected(true);
                case "SE" -> radio_seUpdate.setSelected(true);
                case "IT" -> radio_itUpdate.setSelected(true);
                case "DS" -> radio_dsUpdate.setSelected(true);
            }
            dialog edit = new dialog(this,this,selectedM);
            edit.setVisible(true);

        }
    }//GEN-LAST:event_jButton3MouseClicked

