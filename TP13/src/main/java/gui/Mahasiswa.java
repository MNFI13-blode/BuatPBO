    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        if(!evt.getValueIsAdjusting()){
            String selectedNama = jList1.getSelectedValue();
            if(selectedNama != null){
                try{
                    mahasiswa selectedMaha =  mahService.getNamebyNama(selectedNama);
                    if(selectedMaha != null){
                        jLabel4.setText(selectedMaha.getNim());
                        jLabel5.setText(selectedMaha.getNama());
                        jLabel6.setText(selectedMaha.getProdi());
                    }
                }catch(SQLException ex){
                    JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jList1ValueChanged
    
    private mahasiswa getSelectedMaha(){
        String selectedNama = jList1.getSelectedValue();
        if(selectedNama != null){
            try{
                return mahService.getNamebyNama(selectedNama);
            }catch(SQLException ex) {
                 JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            }
        }
        return null;
    }
    public void reload(){
        listmodel.clear();
        try{
       
         List<mahasiswa>  mahasiswal =  mahService.getAll();
         for(mahasiswa mah : mahasiswal){
             listmodel.addElement(mah.getNama());
         }
         jList1.setModel(listmodel);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }
    
